void tela_menu_aluguel(void){
    char op;
    do{
        op = menu_aluguel();
        switch(op){
            case '1': 	tela_cadastrar_aluguel();
                break;
            case '2': 	tela_pesquisar_aluguel();
                break;
            case '3': 	tela_alterar_aluguel();
                break;
            case '4': 	tela_excluir_aluguel();
                break;
        }
    }while (op != '0');
}

